def remove_stopword(text, stopwords):
    ###### REMOVE stop words
    document = ' '.join('' if word in stopwords else word for word in text.split())
    #print(document)
    ###### DEL excess blank space
    document = regex.sub(r'\s+', ' ', document).strip()
    return document

def preprocess_text_1(text, emoji_dict, teen_dict, wrong_lst, english_dict, stopwords=None):
    # Chuyển đổi Unicode và loại bỏ ký tự đặc biệt
    text = regex.sub(r'\.+', ".", text)
    text = regex.sub(r'[^a-zA-Z0-9\sÀ-ỹ]', ' ', text)
    text = regex.sub(r'\d', '', text)
    
    # Chuẩn hóa Unicode
    text = covert_unicode(text)
    
    # Tách thành câu
    sentences = sent_tokenize(text)
    
    processed_sentences = []
    
    for sentence in sentences:
        # Áp dụng ánh xạ cho EMOJI, TEENCODE và tiếng Anh
        sentence = ''.join(emoji_dict[word.lower()] + ' ' if word.lower() in emoji_dict else word for word in list(sentence))
        sentence = ' '.join(teen_dict[word] if word in teen_dict else word for word in sentence.split())
        sentence = ' '.join(english_dict[word] if word in english_dict else word for word in sentence.split())
        sentence = ' '.join('' if word in wrong_lst else word for word in sentence.split())
        # Loại bỏ từ dừng (stopwords)
        if stopwords is not None:
            sentence = remove_stopword(sentence, stopwords)
        
        processed_sentences.append(sentence)
    
    # Ghi nhận các câu sau khi xử lý
    processed_text = ' '.join(processed_sentences)
    
    # Loại bỏ khoảng trắng thừa
    processed_text = regex.sub(r'\s+', ' ', processed_text).strip()
    
    return processed_text


